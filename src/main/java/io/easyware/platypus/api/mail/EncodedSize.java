package io.easyware.platypus.api.mail;

public class EncodedSize {

    public EncodedSize() {}

    private float plain;
    private float html;

    public float getPlain() {
        return plain;
    }

    public void setPlain(float plain) {
        this.plain = plain;
    }

    public float getHtml() {
        return html;
    }

    public void setHtml(float html) {
        this.html = html;
    }
}
